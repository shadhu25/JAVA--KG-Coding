package _3DataTypes_Variables_InputOutput.Lectures;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("I am\tkrishn");
        System.out.println("I am\b krishn");
        System.out.println("I am\nkrishn");
        System.out.println("I am \"krishn\"");
        System.out.println("I am \\krishn\\");
    }
}
