package _4Operator_ifelse_NumberSystem.Lectures;

public class Arithmetic {
    public static void main(String[] args) {
        int a=8,b=5;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println();
        double x=5.6,y=2.4;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);
    }
}
